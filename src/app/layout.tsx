import type { Metadata } from 'next';
import './globals.css';
import { css } from '@styled-system/css';
import { cookies, headers } from 'next/headers';
import Providers from '@/components/system/Providers';
import Header from '@/components/base/Header';
import { container, containerRaw } from '@/lib/styles/container';
import Footer from '@/components/base/Footer';
import useIsAdmin from '@/lib/store/useIsAdmin';
import process from 'process';

export const metadata: Metadata = {
  title: '기록은 나의 빛',
  description: 'Generated by create next app',
};

function getIsAdmin() {
  const header = headers();
  const adminAddressList = (process.env.ADMIN_ADDRESS || '::1;127.0.0.1').split(
    ';',
  );

  const connectAddress = (header.get('x-forwarded-for') ?? '127.0.0.1').split(
    ',',
  )[0];

  return adminAddressList.includes(connectAddress);
}

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const isAdmin = getIsAdmin();

  useIsAdmin.setState({
    isAdmin,
  });

  return (
    <html lang="ko" data-theme="dark" suppressHydrationWarning>
      <body className={styledBody}>
        <Providers>
          <div className={rootBlock}>
            <Header />
            <div className={mainBlock}>{children}</div>
            <Footer />
          </div>
        </Providers>
      </body>
    </html>
  );
}

const styledBody = css({
  bg: 'bg_page1',
});

const rootBlock = css({
  display: 'flex',
  flexDir: 'column',
  minH: '100dvh',
});

const mainStyle = css.raw({
  flex: 1,
  width: '100%',
  mb: 20,
});

const mainBlock = css(mainStyle, containerRaw);
